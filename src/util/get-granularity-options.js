import { METRIC_GRANULARITY, TIME_PERIOD } from '../constants';

const PERIOD_MAPPINGS = {
  [TIME_PERIOD.DAY]: [METRIC_GRANULARITY.HOUR],
  [TIME_PERIOD.WEEK]: [METRIC_GRANULARITY.HOUR, METRIC_GRANULARITY.DAY],
  [TIME_PERIOD.MONTH]: [METRIC_GRANULARITY.HOUR, METRIC_GRANULARITY.DAY],
  [TIME_PERIOD.YEAR]: [
    METRIC_GRANULARITY.DAY,
    METRIC_GRANULARITY.WEEK,
    METRIC_GRANULARITY.MONTH,
  ],
  [TIME_PERIOD.ALL]: [
    METRIC_GRANULARITY.DAY,
    METRIC_GRANULARITY.WEEK,
    METRIC_GRANULARITY.MONTH,
  ],
};

const getGranularityOptions = (period) => PERIOD_MAPPINGS[period];

export default getGranularityOptions;
